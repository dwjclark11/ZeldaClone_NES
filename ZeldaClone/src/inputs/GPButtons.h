#pragma once
/*
*  Gamepad Button Definitions
*  These buttons are defined based on an
*  X_BOX style Controller and are based on
*  the SDL_GameControllerButton values
*/
#define GP_BTN_INVALID	   -1

// ==============================================
// Action Buttons
// ==============================================
#define GP_BTN_A				0
#define GP_BTN_B				1
#define GP_BTN_X				2
#define GP_BTN_Y				3

// ==============================================
// Menu/Control Buttons
// ==============================================
#define GP_BTN_BACK				4
#define GP_BTN_GUIDE			5
#define GP_BTN_START			6

// ==============================================
// Joystick and Shoulder/Trigger Buttons
// ==============================================
#define GP_BTN_LSTICK			7
#define GP_BTN_RSTICK			8
#define GP_BTN_LSHOULDER		9
#define GP_BTN_RSHOULDER		10

// ==============================================
// Directional Buttons - DPAD
// ==============================================
#define GP_BTN_DPAD_UP			11
#define GP_BTN_DPAD_DOWN		12
#define GP_BTN_DPAD_LEFT		13
#define GP_BTN_DPAD_RIGHT		14

#define GP_BTN_LAST			21




